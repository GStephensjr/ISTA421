namespace cssbs_ex11
{
    class Vector
    {
        public double v1;
        public double v2;
        public double v3;

        public Vector(double v1, double v2)
        {
            this.v1 = v1;
            this.v2 = v2;
        }
        public Vector(double v1, double v2, double v3)
        {
            this.v1 = v1;
            this.v2 = v2;
            this.v3 = v3;
        }
    }
}
